import shap

# Ensure tokenizer and model are set up
from transformers import BertTokenizer, BertForSequenceClassification
tokenizer = BertTokenizer.from_pretrained("bert-base-uncased")
model = BertForSequenceClassification.from_pretrained("bert-base-uncased", num_labels=len(categories))
model = model.eval()  # Ensure model is in evaluation mode

# Initialize SHAP explainer
def predict_proba(inputs):
    """
    Custom function to predict probabilities from tokenized inputs.
    """
    tokenized_inputs = tokenizer(inputs, truncation=True, padding=True, max_length=128, return_tensors="pt")
    with torch.no_grad():
        outputs = model(**tokenized_inputs)
        probabilities = torch.nn.functional.softmax(outputs.logits, dim=-1)
    return probabilities.cpu().numpy()

explainer = shap.Explainer(predict_proba, tokenizer)

# Generate explanations for a single input
input_text = "This website provides streaming media content."
shap_values = explainer([input_text])  # Pass raw text, SHAP will tokenize internally

