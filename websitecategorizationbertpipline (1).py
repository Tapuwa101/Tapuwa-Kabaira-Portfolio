def train_model(model, data_loader, optimizer, loss_fn, device):
    model.train()
    total_loss = 0
    for batch in data_loader:
        optimizer.zero_grad()
        input_ids = batch['input_ids'].to(device)
        attention_mask = batch['attention_mask'].to(device)
        labels = batch['label'].to(device)

        outputs = model(input_ids=input_ids, attention_mask=attention_mask, labels=labels)
        loss = outputs.loss
        loss.backward()
        optimizer.step()
        total_loss += loss.item()

    return total_loss / len(data_loader)  # Return the average loss for the epoch

# Initialize a list to track training losses
training_losses = []

# Train the model
for epoch in range(3):  # Train for 3 epochs
    train_loss = train_model(model, train_loader, optimizer, loss_fn, device)
    training_losses.append(train_loss)  # Save loss for the epoch
    print(f'Epoch {epoch + 1}, Training Loss: {train_loss}')  # Print loss for the epoch

